// Utility function: validate a level before adding
function validateLevel(name, grid) {
  const errors = [];

  if (grid.length !== 18) errors.push(`${name}: Expected 18 rows, got ${grid.length}`);

  grid.forEach((row, i) => {
    if (row.length !== 96) errors.push(`${name} row ${i}: Expected 96 chars, got ${row.length}`);
  });

  if (grid[17] !== GROUND_ROW) errors.push(`${name}: Row 17 (GROUND_ROW) corrupted or missing`);

  let spawnCount = 0, flagCount = 0;
  for (let i = 0; i < 17; i++) {
    for (let j = 0; j < 96; j++) {
      if (grid[i][j] === 'S') spawnCount++;
      if (grid[i][j] === 'F') flagCount++;
    }
  }
  if (spawnCount !== 1) errors.push(`${name}: Expected 1 spawn (S), got ${spawnCount}`);
  if (flagCount !== 1) errors.push(`${name}: Expected 1 flag (F), got ${flagCount}`);

  // Check enemies on ground
  for (let i = 0; i < 17; i++) {
    for (let j = 0; j < 96; j++) {
      if (grid[i][j] === 'E') {
        if (GROUND_ROW[j] !== '#') console.warn(`  ⚠ ${name}: Walker at (col ${j}, row ${i}) over pit (GROUND[${j}]='${GROUND_ROW[j]}')`);
      }
    }
  }

  if (errors.length === 0) {
    console.log(`✓ ${name} is valid`);
    return true;
  } else {
    errors.forEach(e => console.error(`✗ ${e}`));
    return false;
  }
}
